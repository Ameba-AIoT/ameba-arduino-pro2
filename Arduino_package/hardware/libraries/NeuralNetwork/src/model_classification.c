static void img_rgb2gray(img_t *img)
{
    // planar rgb to gray
    uint8_t *r = img->data;
    uint8_t *g = img->data + img->width * img->height;
    uint8_t *b = img->data + img->width * img->height * 2;
    uint8_t *gray = img->data;

    for (int i = 0; i < img->width * img->height; i++) {
        gray[i] = (r[i] * 19595 + g[i] * 38469 + b[i] * 7472) >> 16;
    }
}

