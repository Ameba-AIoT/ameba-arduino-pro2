typedef struct data_format_s {
    nn_tensor_format_t *format;
    nn_tensor_dim_t *dim;
} data_format_t;

static int prob_comparator(const void *pa, const void *pb)
{
    classification_res_t *a = (classification_res_t *)pa;
    classification_res_t *b = (classification_res_t *)pb;
    float diff = a->prob - b->prob;
    if (diff < 0) {
        return 1;
    } else if (diff > 0) {
        return -1;
    }
    return 0;
}

classification_res_t *class_ret = NULL;
int classification_postprocess(void *tensor_out, nn_tensor_param_t *param, void *res)
{
    void **tensor = (void **)tensor_out;
    __fp16 *tensor_out_ptr = (__fp16 *)tensor[0];

    data_format_t fmt;
    fmt.format = &param->format[0];
    fmt.dim = &param->dim[0];
    int class_cnt = fmt.dim->size[0];
    // printf("class_cnt = %d\r\n", class_cnt);

    if (class_ret == NULL) {
        class_ret = malloc(class_cnt * sizeof(classification_res_t));
        if (!class_ret) {
            printf("postprocess : out of resource\n\r");
            return 0;
        }
        memset(class_ret, 0, class_cnt * sizeof(classification_res_t));
    }

    for (int i = 0; i < class_cnt; i++) {
        class_ret[i].prob = (float)tensor_out_ptr[i];
        class_ret[i].class_id = i;
    }

    qsort(class_ret, class_cnt, sizeof(classification_res_t), prob_comparator);

    classification_res_t *class_res = (classification_res_t *)res;
    if (class_res == NULL) {
        printf("postprocess : out of resource\n\r");
        return 0;
    }

    for (int i = 0; i < class_cnt; i++) {
        // printf("class %d, prob %0.2f\n\r", class_ret[i].class_id, class_ret[i].prob);
        class_res[i] = class_ret[i];    // copy data to class_res for nn_set_object()
    }

    return class_cnt;
}


